# Variable initialization
a = 10
b = 3

# --- Arithmetic Operators ---
addition = a + b              # 13
subtraction = a - b          # 7
multiplication = a * b       # 30
division = a / b             # 3.333...
floor_division = a // b      # 3
modulus = a % b              # 1
exponentiation = a ** b      # 1000

print(f"Addition: {addition}")
print(f"Subtraction: {subtraction}")
print(f"Multiplication: {multiplication}")
print(f"Division: {division}")
print(f"Floor Division: {floor_division}")
print(f"Modulus: {modulus}")
print(f"Exponentiation: {exponentiation}")

# --- Comparison Operators ---
is_equal = a == b            # False
is_not_equal = a != b        # True
is_greater = a > b           # True
is_less = a < b              # False
is_greater_equal = a >= b    # True
is_less_equal = a <= b       # False

print(f"Equal: {is_equal}")
print(f"Not equal: {is_not_equal}")
print(f"Greater than: {is_greater}")
print(f"Less than: {is_less}")
print(f"Greater or equal: {is_greater_equal}")
print(f"Less or equal: {is_less_equal}")

# --- Logical Operators ---
result_and = True and False  # False
result_or = True or False    # True
result_not = not True        # False

print(f"AND: {result_and}")
print(f"OR: {result_or}")
print(f"NOT: {result_not}")
